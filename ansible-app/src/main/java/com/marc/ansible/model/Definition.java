package com.marc.ansible.model;

public class Definition {
    private String word;
    private String def;

    public void setWord(String word)
    {   this.word = word; }
    public String getWord()
    {   return word; }

    public void setDef(String def)
    {   this.def = def; }
    public String getDef()
    {   return def; }
}
